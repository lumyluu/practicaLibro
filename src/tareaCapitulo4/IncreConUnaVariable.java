//Escriba cuatro instrucciones distintas en Java, en donde cada una sume 1 a la variable entera x.

public class IncreConUnaVariable {
    public static void main (String [] args){
     int x = 0;

     System.out.printf ("%d%n", x++);
     System.out.printf ("%d%n", x);

     System.out.printf ("%d%n", ++x);

     x = +1;
     System.out.printf ("%d%n", x);

     x = x +1;
     System.out.printf ("%d%n", x);




    }
}
