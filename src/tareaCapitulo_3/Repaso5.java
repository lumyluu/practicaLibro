// Escriba una aplicación que muestre los números del 1 al 4 en la misma línea,
// con cada par de números adyacentes separado por un espacio. Use las siguientes técnicas:

public class Repaso5 {
    public static void main(String[] args) {
        // a) Mediante una instrucción System.out.println.
        // 1 2 3 4
        System.out.println("1 2 3 4");

        // Mediante cuatro instrucciones System.out.print.
        System.out.print("1 ");
        System.out.print("2 ");
        System.out.print("3 ");
        System.out.print("4 ");

        // Mediante una instrucción System.out.printf.

        System.out.printf ("%n%s %s %s %s", 1,2,3,4 );

    }
}
